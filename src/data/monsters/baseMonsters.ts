// ===== UTILITY FUNCTIONS =====

/**
 * Get monsters appropriate for a floor
 */
export function getMonstersForFloor(floorNumber: number): BaseMonster[] {
  return ALL_BASE_MONSTERS.filter(
    (m) => m.minFloor <= floorNumber && (!m.maxFloor || m.maxFloor >= floorNumber)
  );
}

/**
 * Get a random monster for a floor
 */
export function getRandomMonsterForFloor(floorNumber: number): BaseMonster {
  const available = getMonstersForFloor(floorNumber);
  if (available.length === 0) {
    return GOBLIN; // Fallback
  }
  return available[Math.floor(Math.random() * available.length)];
}

/**
 * Get prefix appropriate for floor
 */
export function getRandomPrefixForFloor(floorNumber: number): MonsterPrefix | undefined {
  const available = PREFIXES.filter((p) => p.minFloor <= floorNumber);
  if (available.length === 0 || Math.random() > 0.5) {
    return undefined; // 50% chance of no prefix
  }
  return available[Math.floor(Math.random() * available.length)];
}

/**
 * Get suffix appropriate for floor
 */
export function getRandomSuffixForFloor(floorNumber: number): MonsterSuffix | undefined {
  const available = SUFFIXES.filter((s) => s.minFloor <= floorNumber);
  if (available.length === 0 || Math.random() > 0.4) {
    return undefined; // 60% chance of no suffix
  }
  return available[Math.floor(Math.random() * available.length)];
}
